fromBetaCode :: BS.ByteString -> Maybe BS.ByteString
fromBetaCode input = snd <$> runParser fromParser (BS.unpack input)

toBetaCode :: BS.ByteString -> Maybe BS.ByteString
toBetaCode input = snd <$> runParser toParser (BS.unpack input)

fromParser :: Parser BS.ByteString
fromParser = BS.pack . join <$> (many $ foldl (<|>) (head fromParsers) (tail fromParsers))

toParser :: Parser BS.ByteString
toParser = BS.pack . join <$> (many $ foldl (<|>) (head toParsers) (tail toParsers))

fromParsers :: [Parser [Word8]]
fromParsers = (uncurry buildParser) <$> codes

toParsers :: [Parser [Word8]]
toParsers = (uncurry . flip $ buildParser) <$> codes

buildParser :: [Word8] -> [Word8] -> Parser [Word8]
buildParser pattern token = convert <$> parser
  where parser    = stringP pattern
        convert _ = token

