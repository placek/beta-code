fromBetaCode :: BS.ByteString -> BS.ByteString
fromBetaCode a = foldl encode a codes
  where encode :: BS.ByteString -> (BS.ByteString, String) -> BS.ByteString
        encode s (a, b) = BSL.toStrict $ BSS.replace a (TE.encodeUtf8 $ T.pack b) s

